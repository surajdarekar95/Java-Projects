package JavaPrograms;

public class SwitchCase {

	public static void main(String[] args) {
		
		int day=15;
		
		
		switch(day) {
		case 1:System.out.println("it's a jan");break;
		case 2:System.out.println("it's a Feb");break;
		case 3:System.out.println("it's a March");break;
		case 4:System.out.println("it's a April");break;
		case 5:System.out.println("it's a May");break;
		case 6:System.out.println("it's a June");break;
		case 7:System.out.println("it's a July");break;
		case 8:System.out.println("it's a August");break;
		case 9:System.out.println("it's a September");break;
		case 10:System.out.println("it's a Oct");break;
		case 11:System.out.println("it's a Nov");break;
		case 12:System.out.println("it's a Dec");break;
		default:System.out.println("Enter valid number");


		}
		
		
	}

}
